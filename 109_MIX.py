# convert 
# 1. tuple to list

t1 = (1,2,3,4,5,6,7)
l1 = list(t1)
print(l1)

# 2. tuple to set

s1 = set(t1)
print(s1)

# 3. set to list

s1 = {1,2,3,4,5}
l2 = list(s1)
print(l2)

# 4. set to tuple 

t2 = tuple(s1)
print(t2)

# 5. list to tuple

l3 = [1,2,3,4,5,6,7,8]
t3 = tuple(l3)
print(t3)

#  list to set 

s4 = set(l3)
print(s4)

